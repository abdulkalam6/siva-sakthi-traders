    document.addEventListener('DOMContentLoaded', function() {
        // Add event listeners to all shop buttons
        document.querySelectorAll('.shopButton').forEach(button => {
            button.addEventListener('click', function(event) {
                event.preventDefault();
                const productName = this.getAttribute('data-product-name');
                
                // Debugging log to ensure product name is correctly retrieved
                console.log('Product Name:', productName);
                
                if (productName) {
                    sendWhatsAppMessage(productName);
                } else {
                    console.error('Product name is missing for this button.');
                }
            });
        });
    });
</script>

