    // Function to handle WhatsApp message generation and redirection
    function sendWhatsAppMessage(productName) {
        const whatsappNumber = '8807865786';
        const message = `I would like to purchase the following product: ${productName}`;

        // Generate WhatsApp API link
        const whatsappURL = `https://wa.me/${whatsappNumber}?text=${encodeURIComponent(message)}`;

        // Open WhatsApp with the generated message
        window.open(whatsappURL, '_blank');
    }

